// sizeof.cpp
#include <iostream>
using namespace std;
int main()
{
	int a;
	float b;
	double c;
	bool d;
	char e;
	cout << "\n size of int : " << sizeof(int);
	cout << "\n size of float : " << sizeof(b);
	cout << "\n size of double : " << sizeof(double);
	cout << "\n size of bool : " << sizeof(d);
	cout << "\n size of char : " << sizeof(char);
	return 0;
}